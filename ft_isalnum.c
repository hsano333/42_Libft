int		ft_isalnum(int c)
{
	if (c >= 'a' && c <= 'z')
		return (TRUE);
	else if (c >= 'A' && c <= 'Z')
		return (TRUE);
	else if (c >= '0' && c <= '9')
		return (TRUE);
	return (FALSE);
}
